    private void incluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_incluirActionPerformed
        try {
            AtivoFacade facade = new AtivoFacade();
            if (nomeEvento.getText().isEmpty()) {
                Funcoes.warn("Ativo", "Preencha o campo 'Nome'");
            } else if (!facade.confereAtivo(nomeEvento.getText())) {
                Funcoes.warn("Ativo", "Ativo já cadastrado");
            } else {
                Ativo novo = new Ativo();
                novo.setNome(nomeEvento.getText());
                novo.setDescricao(descricao.getText());
                novo.setIdempresa(model.getEmpresa());
                List<Ativo> lista = model.getListaAtivo();
                lista.add(novo);
                model.getEmpresa().setAtivoList(lista);
                model.setListaAtivo(lista);
                //new EmpresaFacade().salvarEmpresa(model.getEmpresa());
                facade.salvarAtivo(novo);
                nomeEvento.setText("");
                descricao.setText("");
                nomeEvento.requestFocus();
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            Funcoes.erro("Erro", ex.getMessage());
        }
}//GEN-LAST:event_incluirActionPerformed

    private void sairActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sairActionPerformed
        try {
            Empresa empresa = model.getEmpresa();
            empresa.setAtivoList(model.getListaAtivo());
            new EmpresaFacade().salvarAtualizarEmpresa(empresa);
            if (depende) {
                dialog.dispose();
            } else {
                this.dispose();
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            Funcoes.erro("Ativo", ex.getMessage());
        }
    }//GEN-LAST:event_sairActionPerformed

