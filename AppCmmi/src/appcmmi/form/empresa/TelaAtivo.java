    private void excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirActionPerformed
        try {
            if (model.getProjeto() != null) {
                int opcao = Funcoes.opcao("Exclusão", "Deseja excluir o ativo " + model.getProjeto().getNome() + "?");
                if (opcao == 0) {
                    Ativo apaga = model.getProjeto();
                    List<Ativo> lista = model.getListaAtivo();
                    lista.remove(apaga);
                    model.getEmpresa().getAtivoList().remove(apaga);
                    model.setListaAtivo(lista);
                    new AtivoFacade().excluirAtivo(apaga);
                }
            } else {
                Funcoes.erro("Ativo", "Selecione um ativo");
            }
        } catch (Exception e) {
            Funcoes.erro("Erro", e.getMessage());
        }
        model.excluirProjeto();
    }//GEN-LAST:event_excluirActionPerformed

